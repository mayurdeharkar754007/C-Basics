#include <iostream>
using namespace std;

int main() {
    // Integer type
    int age = 25;
    
    // Floating-point type
    double price = 19.99;
    
    // Character type
    char grade = 'A';
    
    // Boolean type
    bool isPassed = true;
    
    // Wide character type
    wchar_t symbol = L'Ω';
    
    // Output values
    cout << "Age: " << age << endl;
    cout << "Price: " << price << endl;
    cout << "Grade: " << grade << endl;
    cout << "Passed: " << isPassed << endl;
    cout << "Symbol: " << symbol << endl;
    
    return 0;
}   